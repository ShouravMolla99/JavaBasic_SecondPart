
//Various Kinds of Mathematical Operation

package math_class;
import java.lang.Math;
public class MathDemo {
    public static void main(String[] args) {
        System.out.println("Number is: "+Math.abs(-9.5));
        System.out.println("Number is: "+Math.sqrt(25));
        System.out.println("Number is: "+Math.pow(8,2));
        System.out.println("Value of PI is:"+Math.PI);
    }
}
